package com.entity;

public class Reader {
    public String id;
    public String name;
    public String sex;
    public Integer age;
    public String telephone;
    public String email;
    public String password;
    public String start;
    public String end;
    public Integer punishcount;
    public Integer lendcount;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public Integer getPunishcount() {
        return punishcount;
    }

    public void setPunishcount(Integer punishcount) {
        this.punishcount = punishcount;
    }

    public Integer getLendcount() {
        return lendcount;
    }

    public void setLendcount(Integer lendcount) {
        this.lendcount = lendcount;
    }
}
